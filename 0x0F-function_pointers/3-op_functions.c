#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - function that returns a sum
 * @a: input
 * @b: input
 * Return: 0;
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that subtracts
 * @a: input
 * @b: input
 * Return: 0;
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that multiplies
 * @a: input
 * @b: input
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that divides
 * @a: input
 * @b: input
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function that returns the remainder of a division
 * @a: input
 * @b: input
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
